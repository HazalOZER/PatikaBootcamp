package week1;

public class C30_For {
    public static void main(String[] args) {

        for (int i = 3; i <=10 ; i+=2) {
            System.out.println(i);
        }

        int k=3;
        System.out.println("------------");

        while (k<=10){System.out.println(k);
            k +=2;}

    }
}
