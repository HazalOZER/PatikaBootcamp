package week2;

public class C29_MathsSinifi {
    public static void main(String[] args) {

        System.out.println(Math.abs(-100));//mutlak değer
        System.out.println(Math.cbrt(8));//küp kök
        System.out.println(Math.ceil(1.3));//Küsüratı yukarıya yuvarlıyor. 2
        System.out.println(Math.floor(1.9));//Küsüratı aşağı doğru yuvarlar 1
        System.out.println(Math.log10(100));//2
        System.out.println(Math.max(100,200));// 200
        System.out.println(Math.pow(2,3));//8
        System.out.println((int)(Math.random()*100));//0-10 arası değer üretir
        System.out.println(Math.round(10.49));//Aşaüı veya yukarı tam sayıya yuvarlar
        System.out.println(Math.rint(10.59));//Round gibi ama double değer döndürür
        System.out.println(Math.signum(-1111));//-1 veya +1 döndürür. sayının işaretidir
        System.out.println(Math.sqrt(4));// karakekökü 2


    }
}
